	private void CobrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CobrarActionPerformed
        if(SelMetP.getSelectedIndex()!=0)
        {
            if(Paciente.getSelectedIndex()!=-1)
            {
                if(usuario.getPacienteSel().getDeuda())
                {
                    String s = usuario.getPacienteSel().Pagar();
                    Pagado p = new Pagado(usuario,s);
                    p.setVisible(true);
                    usuario.getPacienteSel().setDeuda(false);
                    @SuppressWarnings("rawtypes")
					DefaultListModel cb2= new DefaultListModel();
                    int b = 0;
                    b=usuario.getAllPacientes().size();
                    String aux="";
                    String aux2="";
                    Paciente.setModel(cb2);
                    for(int i = 0; i<b;i++)
                    {   
                        try
                        {
                            aux = usuario.getAllPacientes().get(i).getNombre()+" "+usuario.getAllPacientes().get(i).getApellido()+" "+usuario.getAllPacientes().get(i).getDni();
                            if(usuario.getAllPacientes().get(i).getDeuda())
                            {
                                aux2=" Adeuda"; 
                            }
                            else
                            {
                                aux2=" No Adeuda";   
                            }
                            aux += aux2;
                        }
                        catch (Exception e){}
                        cb2.addElement(aux);
                    }
                }
                else
                {
                    NoAdeuda n = new NoAdeuda(usuario);
                    n.setVisible(true);
                }     
            }
        }
        else
        {
            CamposVacios c = new CamposVacios (usuario);
            c.setVisible(true);
        }
    }//GEN-LAST:event_CobrarActionPerformed


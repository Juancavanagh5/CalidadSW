    @SuppressWarnings("unlikely-arg-type")
	private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String a="";
        String pass;
        boolean correcto = false; 
           if(TipoUser.getSelectedIndex()==0)
           {
               IDtext.setText(null);
               PassText.setText(null);
               SelUsuario sel = new SelUsuario();
               sel.setVisible(true);
           }
           else
           {
               if(!(!(PassText.getText().equals(""))&&!(IDtext.equals(""))))
           {
               CamposVacios cv = new CamposVacios(usuario);
               cv.setVisible(true);
               this.PassText.setText(null);
               this.IDtext.setText(null);
           }
               else
           {
               try{
               a=IDtext.getText();
               }catch(Exception e){};
               if(TipoUser.getSelectedIndex()==1)
                {   
                    for(int i = 0;i<usuario.getMisObservers().size();i++)
                    {
                        pass=usuario.getMisObservers().get(i).getPassword();
                        if((pass.equals(PassText.getText()))&&(usuario.getMisObservers().get(i).getID().equals(a)))
                        {   
                            correcto = true;
                            usuario.SelDoctor(usuario.getMisObservers().get(i).getID());
                            //usuario.getMisObservers().get(i).setSelect();
                        }
                    }
                    if(correcto)
                    {
                        PrincipalDoctor open = new PrincipalDoctor(usuario);
                        open.setVisible(true);
                        this.setVisible(false);
                    }
                    else
                    {
                        ErrorAuten error = new ErrorAuten();
                        error.setVisible(true);
                        IDtext.setText(null);
                        PassText.setText(null);
                    }
                }
                if(TipoUser.getSelectedIndex()==2)
                {
                    pass=usuario.getPassword();
                    if((pass.equals(PassText.getText()))&&(usuario.getID().equals(a)))
                    {
                        correcto = true;
                        usuario.setSelect();
                    }
                    if(correcto)
                    {
                        PrincipalAdmin abrir = new PrincipalAdmin(usuario);
                        abrir.setVisible(true);
                        this.setVisible(false);
                    }
                    else
                    {
                        ErrorAuten error = new ErrorAuten();
                        error.setVisible(true);
                        IDtext.setText(null);
                        PassText.setText(null);
                    }
                }
           }
           }
            
    }//GEN-LAST:event_jButton2ActionPerformed


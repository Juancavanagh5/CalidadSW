    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String aux[] = new String [4];
        aux[0] = NomDoc.getText();
        aux[1] = IDDoc.getText();
        aux[2] = PassDoc.getText();
        aux[3] = PassGes.getText();
        if(aux[0].equals("")||aux[1].equals("")||aux[2].equals("")||aux[3].equals(""))
        {
            CamposVacios c = new CamposVacios(usuario);
            c.setVisible(true);
            NomDoc.setText(null);
            IDDoc.setText(null);
            PassDoc.setText(null);
            PassGes.setText(null);
        }
        else
        {
            if(aux[3].equals("4321"))
            {
                new Doctor(aux[0],aux[1],aux[2],usuario);
                NomDoc.setText(null);
                IDDoc.setText(null);
                PassDoc.setText(null);
                PassGes.setText(null);
                this.setVisible(false);
                DCorrecto d = new DCorrecto(usuario);
                d.setVisible(true);
            }
            else
            {
                ErrorAuten v = new ErrorAuten();
                v.setVisible(true);
                NomDoc.setText(null);
                IDDoc.setText(null);
                PassDoc.setText(null);
                PassGes.setText(null);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

